                    await conn.execute(f"CREATE INDEX IF NOT EXISTS {index_name} ON {table}({column})")
                    logger.debug(f"‚úÖ –Ü–Ω–¥–µ–∫—Å {index_name} —Å—Ç–≤–æ—Ä–µ–Ω–æ")
